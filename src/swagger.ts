import swaggerJSDoc from 'swagger-jsdoc';

const swaggerOptions = {
  definition: {
    openapi: '3.0.0',
    info: {
      title: 'Todo App',
      version: '1.0.0',
    },
    components: {
      securitySchemes: {
        bearerAuth: {
          type: 'http',
          scheme: 'bearer',
          bearerFormat: 'JWT',
        },
      },
    },
    security: [
      {
        bearerAuth: [],
      },
    ],
  },
  apis: ['./src/docs/**/*.ts'],
};

const swaggerSpec = swaggerJSDoc(swaggerOptions);

console.log(`The Todo App APIs docs server on ${process.env.HOST}:${process.env.PORT}/api-docs`)

export default swaggerSpec;
